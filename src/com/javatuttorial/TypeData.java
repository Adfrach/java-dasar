package com.javatuttorial;

public class TypeData {
		public static void main (String[] args) {
			//Type Data
			
			// Byte Max 127
			byte iniByte = 100;
			System.out.println(iniByte);
			
			// Short
			short iniShort = 1000;
			System.out.println(iniShort);
			
			//Integer
			int iniInt = 10000000;
			System.out.println(iniInt);
			
			//Long
			long iniLong = 100000000;
			System.out.println(iniLong);
			long iniLong1 = 1000000000L;
			System.out.println(iniLong1);
			
			// float 
			float iniFloat = 10.10f;
			System.out.println(iniFloat);
			
			//double
			double iniDouble = 10.10;
			System.out.println(iniDouble);
			
			// literals
			int decimalInt = 34;
			System.out.println(decimalInt);
			int hexaDecimal = 0xffffff;
			System.out.println(hexaDecimal);
			int binaryDecimal = 0b10101010;
			System.out.println(binaryDecimal);
			
			//Character
			char a = 'a';
			char d = 'd';
			char i = 'i';
			System.out.println(a);
			System.out.println(d);
			System.out.println(i);
			
			//Boolean
			boolean benar = true;
			boolean salah = false;
			System.out.println(benar);
			System.out.println(salah);
			
			//String
			String firstName = "Adithya";
			String lastName = "Frachmansyah";
			String fullName = firstName + " " + lastName;
			System.out.println(firstName);
			System.out.println(lastName);
			System.out.println(fullName);
		}
}
